#include <unistd.h>

int	ft_is_prime(int nb)
{
	int i;

	i = nb / 2;
	if (nb < 2)
		return (0);
	while (i > 1)
	{
		if ((nb % i) == 0)
			return (0);
		else
			i--;
	}
	return (1);
}
