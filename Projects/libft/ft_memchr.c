#include "libft.h"

void	*ft_memchr(const void *src, int c, size_t n)
{
	size_t	i;
	char	*str;

	str = (char*)src;
	i = 0;
	while (i < n)
	{
		if (str[i] == (char)c)
			return (str + i);
		i++;
	}
	return (NULL);
}
